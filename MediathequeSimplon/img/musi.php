                 <h3>Mendelssohn : la m&eacute;lodie de l&#39;altruisme</h3>

<p>Certaines musiques rendent-elles g&eacute;n&eacute;reux ? D&#39;autres &eacute;go&iuml;stes ? Les psychologues Rona Fried et Leonard Berkowitz, de l&#39;Universit&eacute; de New York, ont fait &eacute;couter &agrave; des &eacute;tudiants trois types de musiques : une musique calme, une musique stimulante et une musique jug&eacute;e &laquo; d&eacute;sagr&eacute;able &raquo;. La musique calme &eacute;tait Songs without Words, Opus 19 n&deg;1 en Mi mineur et Opus 38 n&deg;4 en La majeur de Felix Mendelssohn ; la musique stimulante &eacute;tait un air de Duke Ellington (One o&#39;clock jump) et la musique per&ccedil;ue comme d&eacute;sagr&eacute;able &eacute;tait Meditations de John Coltrane.</p>

<p>Apr&egrave;s l&#39;audition, chaque &eacute;tudiant pouvait rentrer chez lui, mais au moment o&ugrave; il s&#39;appr&ecirc;tait &agrave; le faire, l&#39;exp&eacute;rimentatrice lui annon&ccedil;ait qu&#39;elle avait un service &agrave; lui demander : il s&#39;agissait de l&#39;aider &agrave; r&eacute;aliser un travail command&eacute; par un professeur de l&#39;universit&eacute; ; elle pr&eacute;tendait avoir tr&egrave;s peu de temps, et sollicitait de l&#39;aide pour se tirer de ce mauvais pas.</p>

<p>En l&#39;absence de musique, 60 pour cent des &eacute;tudiants ont consenti &agrave; l&#39;aider. Apr&egrave;s l&#39;&eacute;coute de Mendelssohn, ce chiffre a atteint 90 pour cent, la musique stimulante ne produisant pas d&#39;am&eacute;lioration notable, et la musique d&eacute;sagr&eacute;able provoquant une baisse de 15 pour cent des bonnes volont&eacute;s. On sait par ailleurs qu&#39;une humeur positive activ&eacute;e par des stimulus ext&eacute;rieurs favorise l&#39;assistance &agrave; autrui, tandis qu&#39;une humeur n&eacute;gative a des effets contraires. La musique intervenant sur l&#39;humeur, les chiffres seraient expliqu&eacute;s par cet amor&ccedil;age de l&#39;humeur.</p>

<p>Autre explication : la capacit&eacute; de la musique &agrave; renforcer les relations sociales. La psychologue Maria Regina et ses coll&egrave;gues de l&#39;Institut de psychologie de Sa&otilde; Paulo au Br&eacute;sil ont r&eacute;v&eacute;l&eacute; que lorsque des enfants &eacute;coutent des chansons typiques de diff&eacute;rents pays (Petit papa No&euml;l, Douce nuit, etc.), ils se parlent plus et jouent davantage ensemble. D&#39;autres &eacute;tudes ont r&eacute;v&eacute;l&eacute; que la musique diffus&eacute;e pendant un don de sang favorise les dons ult&eacute;rieurs, notamment chez des personnes qui ne sont que des donneurs occasionnels ou des premiers donneurs.</p>

<h3>Musique douce : antidouleur</h3>

<p>Certaines musiques semblent att&eacute;nuer la perception de la douleur, par exemple chez le dentiste ou lors d&rsquo;interventions m&eacute;dicales b&eacute;nignes, mais parfois d&eacute;plaisantes. Ainsi, Y. Chan, de l&rsquo;H&ocirc;pital Queen Mary &agrave; Hong Kong, a &eacute;tudi&eacute; des patients subissant une coloscopie (une sonde est introduite dans le tube digestif). Il faisait subir cet examen avec ou sans musique, puis demandait aux patients de remplir des questionnaires destin&eacute;s &agrave; &eacute;valuer la douleur ressentie. On diffusait &agrave; ceux qui &eacute;taient expos&eacute;s &agrave; de la musique des chansons d&rsquo;amour au rythme lent. Y. Chan a montr&eacute; que la musique r&eacute;duisait l&rsquo;intensit&eacute; de la douleur ressentie, ainsi que l&rsquo;&eacute;tat d&rsquo;anxi&eacute;t&eacute;. Il a constat&eacute; que les scores de douleur subjective &eacute;taient nettement r&eacute;duits en pr&eacute;sence de musique.</p>

<p>Ces observations confirment des mesures r&eacute;alis&eacute;es sur les mouvements ou contractions musculaires involontaires qui accompagnent la sensation douloureuse. De telles mesures r&eacute;v&egrave;lent que des adolescentes subissant un examen gyn&eacute;cologique manifestent moins de tensions musculaires (raidissement des muscles des bras et des jambes) ou de comportements de douleur (grimaces) lorsque l&#39;examen se d&eacute;roule dans une ambiance musicale apaisante.</p>

<h3>Musique classique : antiviolence</h3>

<p>Comment minimiser les comportements turbulents des &eacute;l&egrave;ves dans la cantine d&rsquo;un coll&egrave;ge ? Une exp&eacute;rience in&eacute;dite a &eacute;t&eacute; r&eacute;alis&eacute;e par la psychologue Lynne Chalmers et ses coll&egrave;gues, de l&rsquo;Universit&eacute; du Dakota du Nord : ils ont pass&eacute; de la musique classique dans le r&eacute;fectoire et ont observ&eacute; des r&eacute;sultats tout &agrave; fait probants.</p>

<p>Cette exp&eacute;rience a &eacute;t&eacute; conduite aupr&egrave;s de 1 000 enfants scolaris&eacute;s entre le ce2 et le cm2. Pendant une p&eacute;riode de trois mois, l&#39;&eacute;quipe a proc&eacute;d&eacute; &agrave; des mesures du niveau sonore dans le r&eacute;fectoire et du comportement des enfants, selon que l&#39;on diffusait de la musique classique ou de la musique pop pendant le repas. Les r&eacute;sultats ont ainsi r&eacute;v&eacute;l&eacute; que, comparativement &agrave; l&#39;absence de musique, on observe une baisse de six d&eacute;cibels dans le r&eacute;fectoire (soit sept pour cent du volume sonore habituel) lorsque l&#39;on diffuse de la musique classique, et dix d&eacute;cibels (12 pour cent du volume sonore habituel) lorsqu&#39;il s&#39;agit de musique pop. D&egrave;s que l&#39;on arr&ecirc;te la diffusion de la musique, le volume sonore augmente aussit&ocirc;t. En ce qui concerne les comportements des enfants, le personnel de service a mesur&eacute; une baisse spectaculaire de 55 pour cent des gestes de comportement agressif (coups, jets d&#39;objets), des insultes ou impolitesses, de la mauvaise tenue et du non-rangement des affaires lors du d&eacute;part. Cette diminution des comportements agressifs ou turbulents s&#39;observe tout particuli&egrave;rement avec la musique classique. En outre, l&#39;&eacute;quipe a recens&eacute; le nombre d&#39;impacts d&#39;aliments sur les murs en pr&eacute;sence ou en absence de musique classique : une baisse notable du nombre d&#39;impacts a &eacute;t&eacute; constat&eacute;e en situation de musique classique.</p>

<h3>Musique de dessin anim&eacute; : arithm&eacute;tique</h3>

<p>La musique peut am&eacute;liorer vos performances en arithm&eacute;tique ! Par exemple, Susan Hallam et ses coll&egrave;gues de l&rsquo;Institut d&rsquo;&eacute;ducation de Londres ont propos&eacute; des probl&egrave;mes d&rsquo;arithm&eacute;tique &agrave; des enfants de 10 ou 11 ans, soit dans un environnement musical (une musique de type Walt Disney), soit en l&rsquo;absence de musique. La t&acirc;che durait 15 minutes, et les mesures effectu&eacute;es par l&rsquo;&eacute;quipe londonienne ont montr&eacute; que les enfants plong&eacute;s dans l&rsquo;environnement musical ont effectu&eacute; en moyenne 36 probl&egrave;mes contre 27,3 lorsqu&rsquo;ils devaient travailler dans le silence. &Agrave; quand les musiques de Tom &amp; Jerry en classe de coll&egrave;ge pendant les examens ?</p>

<p>Ces r&eacute;sultats confortent d&#39;autres travaux qui &eacute;tablissent un lien entre performances cognitives et musique. Il a ainsi &eacute;t&eacute; d&eacute;montr&eacute; que la musique a des effets calmants aupr&egrave;s d&#39;enfants hyperactifs, et accro&icirc;t leurs performances en math&eacute;matiques, d&#39;autres &eacute;tudes ayant constat&eacute; des effets positifs de la musique de Mozart sur la concentration d&#39;enfants pr&eacute;sentant des difficult&eacute;s.</p>

<h3>Techno : performance physique</h3>

<p>&Agrave; l&#39;heure o&ugrave; les scandales sportifs sur les produits dopants sont quasi quotidiens, un nouveau produit stimulant pourrait voir le jour. Indolore, ind&eacute;tectable, peu co&ucirc;teux et sans risque : la musique.</p>

<p>Nancy Becker et ses coll&egrave;gues du Coll&egrave;ge Ursinus de Pennsylvanie ont fait passer un exercice physique &agrave; des enfants de 9 &agrave; 11 ans, &agrave; des adultes de 18 &agrave; 55 ans et &agrave; des seniors &acirc;g&eacute;s de 60 &agrave; 80 ans. Les participants devaient faire du v&eacute;lo d&#39;appartement pendant deux minutes, et parcourir la plus grande distance pendant ce laps de temps. Certains p&eacute;dalaient sur fond de musique techno, d&#39;autres sur fond de musique douce, d&#39;autres enfin dans le silence. La musique techno se r&eacute;v&egrave;le particuli&egrave;rement &laquo; dopante &raquo; : alors qu&#39;en l&#39;absence de musique, les sujets ont effectu&eacute; en moyenne 1,2 kilom&egrave;tre, ils ont atteint 1,57 kilom&egrave;tre avec de la musique douce, et 1,62 avec de la musique techno, cet effet ayant &eacute;t&eacute; observ&eacute; aussi bien pour les enfants que pour les adultes, m&ecirc;me les plus &acirc;g&eacute;s.</p>

<p>Une seconde exp&eacute;rience a r&eacute;v&eacute;l&eacute; qu&#39;en diffusant de la musique avant l&#39;exercice, mais en l&#39;arr&ecirc;tant juste avant l&#39;exercice, on obtient &eacute;galement une am&eacute;lioration des performances sportives. Pour les chercheurs, le rythme de la musique pr&eacute;pare le corps &agrave; l&#39;effort et &agrave; la r&eacute;sistance, ce que les fanfares militaires savent depuis la nuit des temps...</p>

<h3>Musique et maladie d&#39;Alzheimer</h3>

<p>Dans certains cas, la musique peut &ecirc;tre utilis&eacute;e &agrave; des fins th&eacute;rapeutiques. Melissa Brotons et Susan Koger, de l&rsquo;Universit&eacute; de l&rsquo;Oregon, ont mesur&eacute; l&rsquo;impact de certaines musiques sur les facult&eacute;s cognitives de personnes atteintes de la maladie d&rsquo;Alzheimer. Les musiques &eacute;taient choisies pour leur capacit&eacute; &agrave; stimuler l&rsquo;expression orale chez ces personnes : il s&rsquo;agissait d&rsquo;airs connus, parmi les classiques de la musique populaire am&eacute;ricaine, &agrave; la fois enjou&eacute;s et m&eacute;lodieux : O&rsquo;Susanna, Singing in the Rain ou My old Kentucky. Les patients &eacute;coutaient ces airs deux fois par semaine pendant 30 minutes, et l&rsquo;on observait leur comportement lors de conversations de groupe, ou la fa&ccedil;on dont ils r&eacute;pondaient &agrave; des questions sur les th&egrave;mes habituellement abord&eacute;s par le personnel soignant. Des experts ind&eacute;pendants ont &eacute;valu&eacute; la qualit&eacute; du contenu de la conversation, sa fluidit&eacute; et la fr&eacute;quence des prises spontan&eacute;es de parole.</p>

<p>Cette &eacute;tude a montr&eacute; qu&#39;en pr&eacute;sence de musique, les patients ont une conversation plus fluide, plus riche, moins h&eacute;sitante. Ils prennent la parole plus souvent, et de fa&ccedil;on plus structur&eacute;e, ce qui est d&eacute;terminant tant on sait que les activit&eacute;s langagi&egrave;res sont perturb&eacute;es dans la maladie d&#39;Alzheimer et qu&#39;il est important de les entra&icirc;ner. Des tests compl&eacute;mentaires ont aussi r&eacute;v&eacute;l&eacute; un niveau d&#39;aphasie moins important lors de la phase d&#39;exposition &agrave; la musique, que dans les deux semaines suivantes. Catherine Chambliss et ses coll&egrave;gues du Coll&egrave;ge Ursinus de Pennsylvanie ont mis en &eacute;vidence les m&ecirc;mes effets chez des patients schizophr&egrave;nes &agrave; qui l&#39;on faisait r&eacute;aliser des t&acirc;ches d&#39;habilet&eacute; manuelle.</p>

<p>Comment la musique exerce-t-elle ses effets ? Nancy Jones et Tiffany Field, de l&#39;Universit&eacute; de Miami, ont travaill&eacute; avec des adolescents souffrant de troubles d&eacute;pressifs, qui pr&eacute;sentent une activit&eacute; &eacute;lectrique c&eacute;r&eacute;brale frontale asym&eacute;trique, une suractivit&eacute; frontale droite &eacute;tant associ&eacute;e &agrave; des &eacute;motions n&eacute;gatives et un certain repli sur soi. En posant des &eacute;lectrodes &agrave; la surface du cr&acirc;ne de ces patients, ils ont enregistr&eacute; l&#39;activit&eacute; &eacute;lectrique de leur cerveau avant, pendant et apr&egrave...</p>
        


